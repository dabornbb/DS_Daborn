public class NameHandlers {
    private String name;
    private String sex;
    /**
     * @return the name
     */
    public String getName(){
        return name; 
    }
    /**
     * @param name the name to set
     */
    public void setName(String name){
        this.name = name;
    }
    /**
     * @return the sex
     */
    public String getSex() {
        return sex;
    }
    /**
     * @param sex the sex to set
     */
    public void setSex(String sex) {
        this.sex = sex;
    }
}
